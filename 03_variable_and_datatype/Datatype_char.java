public class Datatype_char {
    public static void main(String[] args) {
        char a1 = 'a'; // string
        char a2 = 97; // ascii
        char a3 = '\u0061'; // unicode

        System.out.println(a1); // a
        System.out.println(a2); // a
        System.out.println(a3); // a
    }
}
